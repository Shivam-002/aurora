export function getAuthToken() {
  return "Bearer " + localStorage.getItem("jwtToken");
}

export const validation_schema_register = Yup.object().shape({
  firstName: Yup.string()
    .required("First Name is required")
    .min(2, "At least 2 characters")
    .max(50, "At most 50 characters"),
  lastName: Yup.string()
    .required("Last Name is required")
    .min(2, "At least 2 characters")
    .max(50, "At most 50 characters"),
  email: Yup.string().email("Invalid email").required("Email is required"),
  gender: Yup.string().required("Gender is required!"),
  country: Yup.string().required("Country is required!"),
  password: Yup.string()
    .required("Password is required")
    .min(8, "Password must be at least 8 characters"),
  // .matches(
  //   /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/,
  //   "At least 8 characters, 1 uppercase, 1 lowercase, 1 digit & 1 special character"
  // ),
});

export const validation_schema_login = Yup.object().shape({
  email: Yup.string().email("Invalid email").required("Email is required"),
  password: Yup.string()
    .required("Password is required")
    .min(8, "Password must be at least 8 characters"),
});

export const validation_schema_playlist = Yup.object().shape({
  playlistName: Yup.string()
    .required("Playlist Name is required")
    .min(2, "At least 2 characters")
    .max(10, "At most 10 characters"),
});

